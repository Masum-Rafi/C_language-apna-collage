// Relational Oparator
// If ans is 1 its true if its 0 then its false

#include <stdio.h>

int main()
{
   printf("%d\n", 2 == 2);
   printf("%d\n", 3 == 2);
   printf("%d\n", 4 > 2);
   printf("%d\n", 2 >= 2);
   printf("%d\n", 2 < 2);
   printf("%d\n", 2 < 5);
   printf("%d\n", 6 != 2);

   return 0;
}